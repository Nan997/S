package com.ut.titulacionentities.entity;

public class Adeudo {
	
	private Integer idAdeudo;

	private Persona persona;

	private int adeudo;

	public Integer getIdAdeudo() {
		return idAdeudo;
	}

	public void setIdAdeudo(Integer idAdeudo) {
		this.idAdeudo = idAdeudo;
	}

	public Persona getPersona() {
		return persona;
	}

	public void setPersona(Persona persona) {
		this.persona = persona;
	}

	public int getAdeudo() {
		return adeudo;
	}

	public void setAdeudo(int adeudo) {
		this.adeudo = adeudo;
	}
	

}
